<template>
<div class="optionBox" v-on:click="click()">
    <img class="optionIcon" src="@/assets/pic/canzone.png" ref="butt" />
    <div class="optionTitle">Canzone</div>
</div>
</template>

// -- =====================================================================================

<script lang="ts">

import { defineComponent, ref }         from "vue";
import * as TS                          from "@/types/types"
import Mixin                            from "@/mixins/mixin"

// -- =====================================================================================

export default defineComponent ( {

// -- =====================================================================================

    name: "Dora",

// -- =====================================================================================

    setup () {

// -- =====================================================================================

        const product = TS.MyProducts.canzone;

        // eslint-disable-next-line
        const butt = ref<HTMLElement>( null as any );

// -- =====================================================================================

        const { me, pulser } = Mixin();

// -- =====================================================================================

        const click = function () {
            pulser( butt );
            me( product );
        }

// -- =====================================================================================

        return { butt, click }

    }

// -- =====================================================================================

} );

// -- =====================================================================================

</script>

